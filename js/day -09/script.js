// Math -- predefined object

console.log(typeof Math); // object

// 1 ,3,6,7,8,99,12

const maxNum = Math.max(1, 3, 6, 7, 8, 99, 12);

console.log(maxNum);

const minNum = Math.min(1, 3, 6, 7, 8, 99, 12);

console.log("Smallest number is ", minNum);

const randomNumber = Math.random(); // this will return random number between 0 to 1

console.log(randomNumber);

const flooredNumber = Math.floor(2.5);

console.log(flooredNumber);

const ceiledNum = Math.ceil(2.5);

console.log(ceiledNum);

const roundOffNumber = Math.round(3.5);

console.log(roundOffNumber);
